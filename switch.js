//Performing simple Week days program using switch
let inp=4

switch(inp){

case 1: document.write("It is Monday Today")
break
case 2: document.write("It is Tuesday Today")
break
case 3: document.write("It is Wednesday Today")
break
case 4: document.write("It is Thursday Today")
break
case 5: document.write("It is Friday Today")
break
case 6: document.write("It is Saturday Today")
break
case 7: document.write("It is Sunday Today")
break
default:document.write("Enter Valid Input")


}